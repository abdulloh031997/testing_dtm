<?php

namespace backend\controllers;

use Yii;
use common\models\Certificate;
use backend\models\CertificateSearch;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;
use kartik\mpdf\Pdf;

/**
 * CertificateController implements the CRUD actions for Certificate model.
 */
class CertificateController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['POST'],
                ],
            ],
        ];
    }

    /**
     * Lists all Certificate models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new CertificateSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single Certificate model.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionView($id)
    {
        $model = $this->findModel($id);
        return $this->renderPartial('view', [
            'model' => $model,
        ]);
    }

    /**
     * Creates a new Certificate model.
     * If creation is successful, the browser will be redirected to the 'view' page.
     * @return mixed
     */
    public function actionCreate()
    {
        $model = new Certificate();

        if ($model->load(Yii::$app->request->post()) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->id]);
        }

        return $this->render('create', [
            'model' => $model,
        ]);
    }

    /**
     * Updates an existing Certificate model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($model->load(Yii::$app->request->post()) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->id]);
        }

        return $this->render('update', [
            'model' => $model,
        ]);
    }
    public function actionPrint($id)
    {
        $this->layout='cer';
        $model = $this->findModel($id);
        return $this->renderPartial('print', [
            'model' => $model,
        ]);
    }
    public function actionAPrint()
    {
        $model = Certificate::find()->where(['ser' =>1])->asArray()->all();
        return $this->renderPartial('a-print', [
            'model' => $model,
        ]);
    }

    /**
     * Deletes an existing Certificate model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionDelete($id)
    {
        $this->findModel($id)->delete();
        return $this->redirect(['index']);
    }
    public function actionSer()
    {
        $id = Yii::$app->request->post('id');
        $ser = Yii::$app->request->post('ser');
        $model = $this->findModel($id);
        if($ser == 0)
        {
            $model->ser = 1;
            $model->save();
        }
        elseif($ser == 1)
        {
            $model->ser = 0;
            $model->save();
        }
    }

    public function actionPerson($psser,$psnum,$imie)
    {

        $this->layout = false;
        \Yii::$app->response->format = Response::FORMAT_JSON;

        if (empty($psser) || empty($psnum) || empty($imie)) {
            $error_message = "SHIR (PNFL),PASPORT SERIYA,PASPORT RAQAM kiriting!";
            return $this->renderAjax('views', [
                'error_message' => $error_message,
            ]);
        }
        $model = new Person();
        $res = $model::getData($psser,$psnum,$imie,1);
        return $this->renderAjax('views', [
            'res' => $res,
        ]);
    }

    /**
     * Finds the Certificate model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Certificate the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Certificate::findOne($id)) !== null) {
            return $model;
        }

        throw new NotFoundHttpException('The requested page does not exist.');
    }
}
